def YTCompare(search_term, title):
    processed_doc1 = " ".join(
        [word for word in search_term.split() if word not in stopwords_manual]
    )
    processed_doc2 = " ".join(
        [word2 for word2 in title.split() if word2 not in stopwords_manual]
    )
    corpus = [processed_doc1, processed_doc2]
    vectorizer = TfidfVectorizer()
    tfidf = vectorizer.fit_transform(corpus)
    similarity_matrix = cosine_similarity(tfidf)[0, 1]
    print(similarity_matrix)
    return similarity_matrix


def addFaces(thurl):
    response = requests.get(thurl)
    img_PIL = Image.open(BytesIO(response.content))
    gray_PIL = Image.open(BytesIO(response.content)).convert("L")
    img = np.array(img_PIL)
    gray = np.array(gray_PIL)
    face_cascade = cv2.CascadeClassifier(
        cv2.data.haarcascades + "haarcascade_frontalface_default.xml"
    )
    eye_cascade = cv2.CascadeClassifier(cv2.data.haarcascades + "haarcascade_eye.xml")
    smile_cascade = cv2.CascadeClassifier(
        cv2.data.haarcascades + "haarcascade_smile.xml"
    )
    faces = face_cascade.detectMultiScale(gray, 1.3, 5)
    eyesz = []
    smilesz = []
    for (x, y, w, h) in faces:
        cv2.rectangle(img, (x, y), (x + w, y + h), (255, 0, 0), 2)
        roi_gray = gray[y : y + h, x : x + w]
        roi_color = img[y : y + h, x : x + w]
        eyes = eye_cascade.detectMultiScale(roi_gray)
        for (ex, ey, ew, eh) in eyes:
            cv2.rectangle(roi_color, (ex, ey), (ex + ew, ey + eh), (0, 255, 0), 2)
            eyesz.append(eyes)
        smiles = smile_cascade.detectMultiScale(roi_gray, 1.8, 20)
        for (sx, sy, sw, sh) in smiles:
            cv2.rectangle(roi_color, (sx, sy), ((sx + sw), (sy + sh)), (0, 0, 255), 2)
            smilesz.append(smiles)

    return len(faces), len(smilesz), len(eyesz)